      private void updateTable() {                                         
        // TODO add your handling code here:
        DefaultTableModel m =(DefaultTableModel)depCeoData.getModel();
        m.setRowCount(0);  
        for(int i=0; i <Helper.arrDepartment.size();i++)
        {
            m.addRow(new java.lang.Object[]{Helper.arrDepartment.get(i).getId(),Helper.arrDepartment.get(i).getName()});
        }
    }  
