    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row =depCeoData.getSelectedRow();
        if(row != -1){
            c.deleteDep(row);
            updateTable();
            JOptionPane.showMessageDialog(null, "Department deleted ", "Success", JOptionPane.PLAIN_MESSAGE);

        }else{
            JOptionPane.showMessageDialog(null, "Select department to delete", "Error", JOptionPane.ERROR_MESSAGE);

        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
            int index = depCeoData.getSelectedRow(); 
            if(index != -1)
            {
                Department d=new Department(Integer.parseInt( String.valueOf(depCeoData.getValueAt(index, 0))),
                String.valueOf( depCeoData.getValueAt(index, 1)));            
                c.editDep(index,d);
                updateTable();
                JOptionPane.showMessageDialog(null, "Departments updated ", "Success", JOptionPane.PLAIN_MESSAGE);
            }else{
                 JOptionPane.showMessageDialog(null, "Select the updated row ", "Error", JOptionPane.ERROR_MESSAGE);
            }


    }//GEN-LAST:event_jButton2ActionPerformed

