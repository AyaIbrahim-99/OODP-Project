    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        resourcesToDelete.removeAllItems();
        for (int i = 0; i < Helper.arrResource.size(); i++) {
            if(Helper.arrResource.get(i).getState().getClass()==availableState.class)
            {
                resourcesToDelete.addItem(Helper.arrResource.get(i).getId()+">"+Helper.arrResource.get(i).getName());
            }
        }
         deleteResource.setVisible(true);
         deleteResource.setSize(600, 350);
    }//GEN-LAST:event_jButton1ActionPerformed

