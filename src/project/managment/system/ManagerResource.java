    private void updateTable() {                                         
        // TODO add your handling code here:
        DefaultTableModel m =(DefaultTableModel)jTable1.getModel();
        m.setRowCount(0);
        availableState s = new availableState();
        for(int i=0; i <Helper.arrResource.size();i++)
        {    
            if(Helper.arrResource.get(i).getState().getClass()== s.getClass()){
            
                 m.addRow(new java.lang.Object[]{
                 Helper.arrResource.get(i).getId(),
                 Helper.arrResource.get(i).getName(),
                 "Available"
                 
                });
            }else{
                 m.addRow(new java.lang.Object[]{
                 Helper.arrResource.get(i).getId(),
                 Helper.arrResource.get(i).getName(),
                 "Reserved"
                 
                });
            }
        }
  
    } 
