    private void dialougeDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dialougeDeleteBtnActionPerformed
        // TODO add your handling code here:
        
        String index = resourcesToDelete.getSelectedItem().toString();
        String []line = index.split(">");
        for (int i = 0; i < Helper.arrResource.size(); i++) {
            if(Helper.arrResource.get(i).getId()== Integer.parseInt(line[0]) && 
                    Helper.arrResource.get(i).getName().equals(line[1]) ){
                Helper.arrResource.remove(i);
            }
        }        
        deleteResource.setVisible(false);
        updateTable();
        
    }//GEN-LAST:event_dialougeDeleteBtnActionPerformed

    private void dialougeAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dialougeAddBtnActionPerformed
        // TODO add your handling code here:
        String name = NameTxt.getText();
        NameTxt.setText("");
        int id = Helper.arrResource.get(Helper.arrResource.size()-1).getId()+1;
        Manager.addResource(new Resource(id, name));
        addResource.setVisible(false);
        updateTable();
        
    }//GEN-LAST:event_dialougeAddBtnActionPerformed

